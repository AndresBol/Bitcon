    private int validarCodigoIngresado(String  HileraIngresada){
        try
        {
            if(!HileraIngresada.equals("")){
                int codigoIngresado = Integer.parseInt(HileraIngresada);
                if(1000 <= codigoIngresado && codigoIngresado <= 1100){
                    
                    return codigoIngresado;
                    
                }else{
                    JOptionPane.showMessageDialog(null, "Favor digite un codigo de tablero mayor igual a 1000 y menor igual a 1100.", "Digitar posiciones del tablero reales", JOptionPane.WARNING_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Favor ingresar un codigo de tablero a buscar", "Favor rellenar los campos solicitados", JOptionPane.WARNING_MESSAGE);
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Favor digitar un formato de dato correspondiente al solicitado", "ERROR de formato", JOptionPane.WARNING_MESSAGE);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Ha sucedido un error.\n" + e, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        
        return 0;
    }
    
    private void ImprimirTablero(){
        
        int codigoTablero = validarCodigoIngresado(Txf_CodigoTablero.getText());
        
        if(codigoTablero != 0){
            Tablero tableroSeleccionado = Juego_ISWBITCON.ObtenerTablero(codigoTablero);
        
            if(tableroSeleccionado.isTableroGanador()){
                Txa_Tablero.setForeground(Color.green);
            }else{
                Txa_Tablero.setForeground(Color.black);
            }

            Txa_Tablero.setText(tableroSeleccionado.ImprimirTablero());
            Lbl_NombreTablero.setText("Nombre del tablero: " + tableroSeleccionado.getNombreTablero());
        }
    }
    
