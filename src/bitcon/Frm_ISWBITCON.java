    private int validarFichaIngresada(String  HileraIngresada){
        try
        {
            if(!HileraIngresada.equals("")){
                int fichaIngresada = Integer.parseInt(HileraIngresada);
                if(1 <= fichaIngresada && fichaIngresada <= 95){
                    
                    return fichaIngresada;
                    
                }else{
                    JOptionPane.showMessageDialog(null, "Numeros de ficha admitidos: mayor igual a 1 y menor igual a 95.", "Digitar valores de fichas reales", JOptionPane.WARNING_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Favor ingresar una ficha a capturar", "Favor rellenar los campos solicitados", JOptionPane.WARNING_MESSAGE);
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Favor digitar un formato de dato correspondiente al solicitado", "ERROR de formato", JOptionPane.WARNING_MESSAGE);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Ha sucedido un error.\n" + e, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        
        return 0;
    }
    
    private void Btn_IngresarFichaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_IngresarFichaActionPerformed
        // TODO add your handling code here:
        int fichaCapturada = validarFichaIngresada(Txf_FichaCapturada.getText());
        
        if(fichaCapturada != 0){
            Juego_ISWBITCON.MarcarTableros(fichaCapturada);
            Txf_FichaCapturada.setText("");
            JOptionPane.showMessageDialog(null, "Ficha #" + fichaCapturada + " marcada en los tableros", "Ficha capturada", JOptionPane.INFORMATION_MESSAGE);
            ImprimirTablero();

            if(!Juego_ISWBITCON.ValidarTableros().equals("")){
                Txa_TablerosGanadores.setText(Juego_ISWBITCON.ValidarTableros());
                ImprimirTablero();
                JOptionPane.showMessageDialog(null, Juego_ISWBITCON.ValidarTableros(), "GANADORES", JOptionPane.INFORMATION_MESSAGE);
                reiniciarJuego();
            }
        }
        
        if(Frm_ConfiguracionJuego.GeneracionFichas == "Aleatorio"){
            Txf_FichaCapturada.setText("" + Juego_ISWBITCON.GenerarNumeroDeFichaAleatorio());
        }
    }//GEN-LAST:event_Btn_IngresarFichaActionPerformed

